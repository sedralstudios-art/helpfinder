function escapeRegex(s) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

let applied = 0, missed = 0, skipped = 0;
const misses = [];

for (const [filename, oldTitle, newTitle] of rewrites) {
  if (skipFiles.has(filename)) { skipped++; continue; }
  const p = path.join(dir, filename);
  if (!fs.existsSync(p)) { misses.push(filename + ' (file missing)'); missed++; continue; }
  const src = fs.readFileSync(p, 'utf8');

  // Match the exact title.en line.
  const re = new RegExp('(title:\\s*\\{\\s*en:\\s*")' + escapeRegex(oldTitle) + '(")');
  if (!re.test(src)) {
    misses.push(filename + ' (expected old title not found)');
    missed++;
    continue;
  }
  const updated = src.replace(re, '$1' + newTitle + '$2');
  fs.writeFileSync(p, updated);
  applied++;
}

